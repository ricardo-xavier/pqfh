package br.com.avancoinfo.pendencias;

public class Detalhes {

	private String chaveAcesso;
	private String nfceSubstituta;
	private String xml;
	public String getChaveAcesso() {
		return chaveAcesso;
	}
	public void setChaveAcesso(String chaveAcesso) {
		this.chaveAcesso = chaveAcesso;
	}
	public String getNfceSubstituta() {
		return nfceSubstituta;
	}
	public void setNfceSubstituta(String nfceSubstituta) {
		this.nfceSubstituta = nfceSubstituta;
	}
	public String getXml() {
		return xml;
	}
	public void setXml(String xml) {
		this.xml = xml;
	}

}
